#include <iostream>
using namespace std;

int main() {
    int a=5, b=4;
    
    cout<<"\n The Operaters are = "<<a+b;
    cout<<"\n The Operaters are = "<<a-b;
    cout<<"\n The Operaters are = "<<a*b;
    cout<<"\n The Operaters are = "<<a/b;
    cout<<"\n The Operaters are = "<<a%b;
    cout<<"\n The Operaters are = "<<a++;
    cout<<"\n The Operaters are = "<<a--;
    cout<<"\n The Operaters are = "<<++a;
    cout<<"\n The Operaters are = "<<--a;


    
    return 0;
}