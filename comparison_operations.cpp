#include <iostream>
using namespace std;

int main() {
    int a=5,b=4;
    cout<<"The Comperation Equal operation = "<<(a==b)<<endl;
    cout<<"The Comperation Greater than or equal to operation = "<<(a>=b)<<endl;
    cout<<"The Comperation Less than or equal to operation = "<<(a<=b)<<endl;
    cout<<"The Comperation Greater than operation = "<<(a>b)<<endl;
    cout<<"The Comperation Less than operation = "<<(a<b)<<endl;
    cout<<"The Comperation Not equal to operation = "<<(a!=b)<<endl;
    return 0;
}